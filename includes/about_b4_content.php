<p>
	 C 1966 г. JUWEL Aquarium производит аквариумы на севере Германии и постоянно совершенствует технологические процессы для производства еще более качественной продукции. Работа сотрудников компании на важных этапах изготовления аквариумов существенно упрощается за счет применения ультрасовременных роботизированных технологических процессов. Большая часть аквариумов полностью склеивается в автоматическом режиме. <br>
